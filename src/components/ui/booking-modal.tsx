"use client";

import * as React from "react";
import { motion, AnimatePresence } from "framer-motion";
import { X, Phone, Mail, Clock, MapPin, Calendar, ArrowRight } from "lucide-react";
import { Button } from "@/components/ui/button";
import { TrustBadge } from "@/components/ui/trust-badge";
import { businessInfo } from "@/lib/constants";

interface BookingModalProps {
    isOpen: boolean;
    onClose: () => void;
}

export function BookingModal({ isOpen, onClose }: BookingModalProps) {
    // Close on Escape key
    React.useEffect(() => {
        const handleEscape = (e: KeyboardEvent) => {
            if (e.key === "Escape") onClose();
        };
        if (isOpen) {
            document.addEventListener("keydown", handleEscape);
            document.body.style.overflow = "hidden";
        }
        return () => {
            document.removeEventListener("keydown", handleEscape);
            document.body.style.overflow = "unset";
        };
    }, [isOpen, onClose]);

    return (
        <AnimatePresence>
            {isOpen && (
                <>
                    {/* Backdrop */}
                    <motion.div
                        className="fixed inset-0 bg-black/60 backdrop-blur-sm z-50"
                        initial={{ opacity: 0 }}
                        animate={{ opacity: 1 }}
                        exit={{ opacity: 0 }}
                        onClick={onClose}
                    />

                    {/* Modal */}
                    <motion.div
                        className="fixed inset-0 z-50 flex items-center justify-center p-4"
                        initial={{ opacity: 0 }}
                        animate={{ opacity: 1 }}
                        exit={{ opacity: 0 }}
                    >
                        <motion.div
                            className="bg-background rounded-2xl shadow-2xl max-w-lg w-full overflow-hidden border border-border"
                            initial={{ scale: 0.95, y: 20 }}
                            animate={{ scale: 1, y: 0 }}
                            exit={{ scale: 0.95, y: 20 }}
                            transition={{ type: "spring", damping: 25, stiffness: 300 }}
                            onClick={(e) => e.stopPropagation()}
                        >
                            {/* Header */}
                            <div className="bg-foreground text-background p-6 relative">
                                <button
                                    onClick={onClose}
                                    className="absolute top-4 right-4 p-2 rounded-full hover:bg-white/10 transition-colors"
                                    aria-label="Close modal"
                                >
                                    <X className="w-5 h-5" />
                                </button>
                                <div className="flex gap-2 mb-3">
                                    <TrustBadge variant="premium" icon="star">Concierge Booking</TrustBadge>
                                </div>
                                <h2 className="font-serif text-2xl font-semibold">
                                    Book Your Treatment
                                </h2>
                                <p className="text-background/70 text-sm mt-1">
                                    Contact our specialists to schedule your appointment
                                </p>
                            </div>

                            {/* Content */}
                            <div className="p-6 space-y-4">
                                {/* Phone Option */}
                                <a
                                    href={`tel:${businessInfo.phone.replace(/\s/g, "")}`}
                                    className="flex items-start gap-4 p-4 rounded-xl border border-border hover:border-gold/50 hover:bg-gold/5 transition-all group"
                                >
                                    <div className="w-12 h-12 rounded-full bg-gold/10 flex items-center justify-center flex-shrink-0">
                                        <Phone className="w-5 h-5 text-gold" />
                                    </div>
                                    <div className="flex-1">
                                        <h3 className="font-semibold mb-1 group-hover:text-gold transition-colors">
                                            Call Us Directly
                                        </h3>
                                        <p className="text-muted-foreground text-sm mb-1">
                                            Speak with our booking team
                                        </p>
                                        <span className="text-gold font-medium">{businessInfo.phone}</span>
                                    </div>
                                    <ArrowRight className="w-5 h-5 text-muted-foreground group-hover:text-gold group-hover:translate-x-1 transition-all" />
                                </a>

                                {/* WhatsApp Option */}
                                <a
                                    href={`https://wa.me/${businessInfo.phone.replace(/\s/g, "").replace("+", "")}?text=${encodeURIComponent("Hi Galeo Beauty! I found you on www.galeobeauty.com and I'd like to book an appointment.")}`}
                                    target="_blank"
                                    rel="noopener noreferrer"
                                    className="flex items-start gap-4 p-4 rounded-xl border border-border hover:border-green-500/50 hover:bg-green-500/5 transition-all group"
                                >
                                    <div className="w-12 h-12 rounded-full bg-green-500/10 flex items-center justify-center flex-shrink-0">
                                        <svg className="w-5 h-5 text-green-600" viewBox="0 0 24 24" fill="currentColor">
                                            <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
                                        </svg>
                                    </div>
                                    <div className="flex-1">
                                        <h3 className="font-semibold mb-1 group-hover:text-green-600 transition-colors">
                                            WhatsApp Us
                                        </h3>
                                        <p className="text-muted-foreground text-sm mb-1">
                                            Quick and convenient messaging
                                        </p>
                                        <span className="text-green-600 font-medium">Chat Now</span>
                                    </div>
                                    <ArrowRight className="w-5 h-5 text-muted-foreground group-hover:text-green-600 group-hover:translate-x-1 transition-all" />
                                </a>

                                {/* Email Option */}
                                <a
                                    href={`mailto:${businessInfo.email}?subject=Appointment%20Booking%20Request`}
                                    className="flex items-start gap-4 p-4 rounded-xl border border-border hover:border-gold/50 hover:bg-gold/5 transition-all group"
                                >
                                    <div className="w-12 h-12 rounded-full bg-gold/10 flex items-center justify-center flex-shrink-0">
                                        <Mail className="w-5 h-5 text-gold" />
                                    </div>
                                    <div className="flex-1">
                                        <h3 className="font-semibold mb-1 group-hover:text-gold transition-colors">
                                            Email Us
                                        </h3>
                                        <p className="text-muted-foreground text-sm mb-1">
                                            For detailed inquiries
                                        </p>
                                        <span className="text-gold font-medium">{businessInfo.email}</span>
                                    </div>
                                    <ArrowRight className="w-5 h-5 text-muted-foreground group-hover:text-gold group-hover:translate-x-1 transition-all" />
                                </a>
                            </div>

                            {/* Footer */}
                            <div className="px-6 pb-6">
                                <div className="flex items-center gap-4 text-sm text-muted-foreground bg-muted/50 rounded-lg p-4">
                                    <Clock className="w-4 h-4 text-gold flex-shrink-0" />
                                    <div>
                                        <span className="font-medium text-foreground">Hours:</span>{" "}
                                        Mon-Fri 8am-6pm, Sat 8am-4pm
                                    </div>
                                </div>
                            </div>
                        </motion.div>
                    </motion.div>
                </>
            )}
        </AnimatePresence>
    );
}

// Hook to manage modal state
export function useBookingModal() {
    const [isOpen, setIsOpen] = React.useState(false);
    const open = React.useCallback(() => setIsOpen(true), []);
    const close = React.useCallback(() => setIsOpen(false), []);
    return { isOpen, open, close };
}
